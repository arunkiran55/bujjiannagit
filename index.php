<script>
$(document).ready(function () {
  $("form").submit(function(e){
    e.preventDefault();
    $flag=true;
    let checking="";
    $(this).find(".required").each(function(){
      if($(this).val()=="" || $(this).val()==null){
        $flag=false;
        $(this).siblings(".spanerr").html($(this).attr('err'));
        checking="err";
      }else{
        $(this).siblings(".spanerr").html("");
        checking="noerr";
      }
    }); 
    if(checking=="noerr"){
          var data = $("#contactform")[0];
          $.ajax({
            url: "common/ajaxcalls/contactajax.php",
            method: "post",
            enctype:"multipart/form-data",
            data: new FormData(data),
            contentType:false,
            processData:false,
           dataType: 'json',
           Cache:false,
            success:function(res){
                console.log(res)
                    if (res.success) {
                      Swal.fire({
                          icon: "success",
                          title: "Congratulations..",
                          text: res.success,
                      }).then(function() {
                          window.location.href='./index.php'
                      });
                  } else if (res.failed) {
                      Swal.fire({
                          icon: "error",
                          title: "Oops...",
                          text: res.failed,
                      }).then(function() {
                          window.location.href='./index.php'
                      });
                  } else if (res.exists) {
                      Swal.fire({
                          icon: "warning",
                          title: "Oops...",
                          text: res.exists,
                      }).then(function() {
                          window.location.href='./index.php'
                      });
                  }else if (res.format) {
                      Swal.fire({
                          icon: "warning",
                          title: "Oops...",
                          text: res.format,
                      }).then(function() {
                          window.location.href='./index.php'
                      });
                  }
            }
         });
        } 
    return $flag;
      })
   });
</script>